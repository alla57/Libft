#include <stdlib.h>
#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void*))
{
	t_list *tmp;

	while (*lst)
	{
		tmp = *lst;
		*lst = tmp->next;
		(*del)(tmp->content);
		free(tmp);
	}
	*lst = NULL;
}
