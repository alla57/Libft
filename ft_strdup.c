#include <stdlib.h>

char	*ft_strdup(const char *s1)
{
	char	*dest;
	int		i;

	i = 0;
	while (s1[i])
		i++;
	dest = malloc(sizeof(char) * i);
	while (i >= 0)
	{
		dest[i] = s1[i];
		i--;
	}
	return (dest);
}
